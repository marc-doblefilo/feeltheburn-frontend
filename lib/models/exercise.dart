class Exercise{
  String name;
  List<String> muscularGroup;
  String image;
  int difficulty;
  String description;

  Exercise(String name, List<String> muscularGroup, String image, int difficulty, String description) {
    this.name = name;
    this.muscularGroup = muscularGroup;
    this.image = image;
    this.difficulty = difficulty;
    this.description = description;
  }
}